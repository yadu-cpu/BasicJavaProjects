package Looping;

public class SimpleifElse {
	public static void main(String args[])
	{
		int chk_number=600;
		if(chk_number<300)
		{
			System.out.println("Number is less than 300"+" Number is"+ chk_number);
			
		}
		else if(chk_number==300)
		{
			System.out.println("Number is equal to 300"+" Number is "+  chk_number);
		}
		else
		{
			System.out.println("Number is greater than 300"+" Number is " +   chk_number);
			}
	}

}
